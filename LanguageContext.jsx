const LanguageContext = createContext();

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState('fr');

  const setLanguageAndSave = (lang) => {
    setLanguage(lang);
    // You could save the preference to localStorage here
  };

  const t = (key, options) => {
    let translation = (translations[language] && translations[language][key]) || translations['en'][key] || key;
    if (options) {
      Object.keys(options).forEach(k => {
        translation = translation.replace(`{${k}}`, options[k]);
      });
    }
    return translation;
  };

  const value = {
    language,
    setLanguage: setLanguageAndSave,
    t,
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};